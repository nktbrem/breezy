    <p class="tooltip_modal_text">
        <b>Разговорный динамик и датчик приближения</b> <br>
        В момент вызова поднесите ладонь к верхней части экрана, если экран гаснет, значит датчик приближения работает.
    </p>
    <p class="tooltip_modal_text">
        <b>Разъем зарядного устройства</b> <br>
        Чтобы проверить разъем, подключите зарядное устройство. Если разъем работает, на экране отобразится значок молнии рядом со значком аккумулятора в строке состояния, либо крупный значок аккумулятора на экране блокировки. 
        Иногда зарядка не идет, потому что само зарядное устройство неисправно. 
