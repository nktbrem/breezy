    <p class="tooltip_modal_text">
        <b>Основная и фронтальная камеры</b> <br>
        Убедитесь, что камеры фокусируются, и что на фото нет пятен и искажений. Нужно, чтобы сделанные фотографии можно было просмотреть. 
    </p>
    <p class="tooltip_modal_text">
        <b>Кнопки</b> <br>
        Проверьте их работоспособность: <br>
        1. Когда включаете беззвучный режим соответствующим переключателем в верхней части корпуса всегда должен быть виброотклик. <br>
        2. Клавиши изменения громкости нажимаются с характерным щелчком. Во время нажатия должна сразу появиться шкала (ползунок) изменения громкости. <br>
        3. Кнопка “Power” блокирует экран, и также активирует смартфон в режим экрана блокировки. <br>
        4. Кнопка “Home (Домой)”. На смартфонах до iPhone 7 она механическая. Должна работать без скрипа, треска, 
        нажимать должно быть легко. В моделях после iPhone 7 эта кнопка сенсорная и ее тоже нужно проверить.
